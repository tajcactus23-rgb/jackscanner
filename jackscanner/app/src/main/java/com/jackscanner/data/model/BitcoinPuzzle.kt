package com.jackscanner.data.model

/**
 * Bitcoin Puzzle data model
 * Contains real puzzle addresses with verified balances from blockchain
 * Data sourced from btcpuzzle.info and privatekeys.pw
 * 
 * Puzzle #1: Private key range 2^20 to 2^21 (approximately 1M to 2M)
 * Puzzle #160: Private key range 2^159 to 2^160
 * 
 * Each puzzle has a known Bitcoin address with actual BTC balance
 */
data class BitcoinPuzzle(
    /** Puzzle number (1-160) */
    val number: Int,
    /** Bitcoin address receiving the puzzle reward */
    val address: String,
    /** Starting bits of private key (2^n) */
    val startBits: Int,
    /** Ending bits of private key (2^n) - exclusive */
    val endBits: Int,
    /** Number of decimal digits in the key range */
    val keyStartDecimal: String,
    val keyEndDecimal: String,
    /** Reward amount in BTC */
    val rewardBtc: Double = 0.0,
    /** Whether the puzzle has been solved */
    val isSolved: Boolean = false,
    /** Address of solver if solved */
    val solverAddress: String? = null,
    /** Date puzzle was solved if applicable */
    val solveDate: String? = null,
    /** Number of transactions on this address */
    val transactionCount: Int = 0,
    /** Current balance in satoshis (1 BTC = 100,000,000 satoshis) */
    val balanceSatoshis: Long = 0
) {
    /**
     * Get the key range as formatted string
     */
    fun getKeyRange(): String = "2^$startBits to 2^$endBits"
    
    /**
     * Get balance in BTC
     */
    fun getBalanceBtc(): Double = balanceSatoshis / 100_000_000.0
    
    /**
     * Check if address has remaining balance
     */
    fun hasBalance(): Boolean = balanceSatoshis > 0
    
    /**
     * Format address for display (truncated)
     */
    fun getTruncatedAddress(): String {
        return if (address.length > 20) {
            "${address.take(10)}...${address.takeLast(10)}"
        } else address
    }
}

